package com.stratio.examplesTest

import org.scalatest._
import org.scalatest.refspec.RefSpec
import prop._
import scala.collection.immutable._


/**
  * Estilo tipo xUnit. No esta planteado en modo BDD
  */
class MyFunSuite extends FunSuite {

  test("An empty Set should have size 0") {
    assert(Set.empty.size == 0)
  }

  test("Invoking head on an empty Set should produce NoSuchElementException") {
    assertThrows[NoSuchElementException] {
      Set.empty.head
    }
  }
}


/**
  * Acercamiento a BDD (Behavior-driven development). Tests estilo A should B, B must C
  */
class MyFlatSpec extends FlatSpec {

  "An empty Set" should "have size 0" in {
    assert(Set.empty.size == 0)
  }

  it should "produce NoSuchElementException when head is invoked" in {
    assertThrows[NoSuchElementException] {
      Set.empty.head
    }
  }
}


/**
  * Parecido a Ruby's RSpec tool. Permite facilmente estructurar los tests modo especificacion ( con `describe` e `it`)
  */
class MyFunSpec extends FunSpec {

  describe("A Set") {
    describe("when empty") {
      it("should have size 0") {
        assert(Set.empty.size == 0)
      }

      it("should produce NoSuchElementException when head is invoked") {
        assertThrows[NoSuchElementException] {
          Set.empty.head
        }
      }
    }
  }
}

/**
